let decision = prompt('Du spazierst durch einen Wald und kommst an eine Weggabelung. Wohin gehst du?');
// Zeigt eine Variable an und prompt wird der Variable zugewiesen.

if (decision == 'left') {
    alert('Du hast dich dafür entschieden nach links zu gehen und triffst auf einen blauäugigen weißen Drachen, welchen du zähmst.');
}
 else (decision == 'right'); { alert('Du hast dich entschieden nach rechts zu gehen und findest eine myteriöse Frucht, welche dir geheimnisvolle Kräfte verspricht.');
} 

// Bei dem if wird geprüft, ob die Person nach links geht. Wenn nicht wird bei else geprüft, ob sie nach rechts geht. 
// Je nach Entscheidung wird durch alert ein anderer Verlauf der Geschichte ausgegeben. 

 alert('Nun bist du die stärkste Person im ganzen Land.'); 
// Hier wird das Ende der Geschichte angezeigt.